// Switch case
// w a p to print the day of the week based on 1-7
let day = 5;
switch(day){
    case 1:
        console.log("monday");
        break;
    case 1:
         console.log("tuesday");
        break;
    case 3:
         console.log("wednesday");
           break;  
    case 4:
        console.log("thursday");
        break;
    case 5:
        console.log("friday");
           break;
    case 6:
            console.log("saturday");
           break;
    case 7:
            console.log("sunday");
           break;
}

